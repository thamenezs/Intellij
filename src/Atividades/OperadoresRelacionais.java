package Atividades;

public class OperadoresRelacionais {
    public static void main(String[] args){

        int i1 = 20;
        int i2 = 12;
        float f1 = 2.9f;
        float f2 = 3.5f;
        String s1 = "menu";
        String s2 = "manu";
        double d1 = 2.1;
        double d2 = 2.1;

        System.out.println((i1 == i2));
        System.out.println((f1 <= f2));
        System.out.println((s1 != s2));
        System.out.println((d1 == d2));
    }
}
